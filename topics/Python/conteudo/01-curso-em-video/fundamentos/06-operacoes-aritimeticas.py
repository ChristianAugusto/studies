num1 = 5
num2 = 2

print("soma", num1 + num2) #soma
print("sub", num1 - num2) #sub
print("mult", num1 * num2) #mult
print("div", num1 / num2) #div
print("div inteira", num1 // num2) #div inteira
print("pot", num1 ** num2) #pot
print("resto div", num1 % num2) #resto div

