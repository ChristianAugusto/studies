# True or False

print(7 != 3 and 2 > 3)
print("\n\n")

print("and: ")
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print("\n\n")

print("or: ")
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print("\n\n")

print("xor: ")
print(True != True)
print(True != False)
print(False != True)
print(False != False)
print("\n\n")

print("not: ")
print(not True)
print(not False)
print(not 0)
print(not 1)
print("\n\n")